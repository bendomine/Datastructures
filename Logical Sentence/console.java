public class console {
	public static void log(String in){
		System.out.println(in);
	}
	public static void log(boolean in){
		System.out.println(in);
	}
	public static void log(int in){
		System.out.println(in);
	}
	public static void log(double in){
		System.out.println(in);
	}
	public static void log(float in){
		System.out.println(in);
	}
	public static void log(Character in){
		System.out.println(in);
	}
}
